def suma(x,y,z):
    resultado=x+y+z
    return resultado

def multiplicar(x,y):  
    mult=x*y
    return mult
    
R1=suma(7,5,9)
print("Primera Suma con funcion")
print(R1)
R2=suma(8,5,9)
print("Segunda Suma con funcion")
print(R2)
R3=suma(7,4,9)
print("Tercera Suma con funcion")
print(R3)
M1=multiplicar(2,4)
print("Primera Multiplicacion")
print(M1)
M2=multiplicar(5,8)
print("Segunda Multiplicacion")
print(M2)
sumaMul=M1+M2
print("Suma de ambas multiplicaciones anteriores")
print(sumaMul)
